-- Загрузка стандартных настроек NvChad
require "nvchad.options"

-- Диагностические иконки
local signs = { Error = " ", Warn = " ", Hint = " ", Info = " " }
for type, icon in pairs(signs) do
  local hl = "DiagnosticSign" .. type
  vim.fn.sign_define(hl, { text = icon, texthl = hl, numhl = hl })
end

-- Общие параметры для удобства настройки
local opt = vim.opt

-- 1. Отображение
opt.number = true -- Включить нумерацию строк
opt.relativenumber = true -- Включить относительные номера строк
opt.wrap = false -- Отключить перенос строк
opt.signcolumn = "yes" -- Всегда показывать столбец знаков
opt.cursorline = true -- Включить подсветку текущей строки
opt.cursorlineopt = "both" -- Подсвечивать как строку, так и колонку
opt.termguicolors = true -- Включить поддержку 24-битных цветов в терминале

-- 2. Редактирование
opt.tabstop = 2 -- Количество пробелов для табуляции
opt.shiftwidth = 2 -- Размер отступа для автоиндентификации
opt.expandtab = true -- Преобразовывать табы в пробелы
opt.smartindent = true -- Умное авто-индентифицирование
opt.clipboard = "unnamedplus" -- Использовать системный буфер обмена
opt.timeoutlen = 300 -- Тайм-аут для командных последовательностей (в миллисекундах)
opt.undofile = true -- Включить поддержку истории undo
opt.swapfile = false -- Отключить swap файлы (для более безопасной работы)

-- 3. Поиск
opt.ignorecase = true -- Игнорировать регистр при поиске
opt.smartcase = true -- Если есть заглавные буквы в запросе, учитывать регистр
opt.incsearch = true -- Показ совпадений по мере ввода
opt.hlsearch = true -- Подсвечивать все совпадения поиска
opt.inccommand = "nosplit" -- Показывать предварительный просмотр команд в реальном времени

-- 4. Работа с мышью
opt.mouse = "a" -- Включить поддержку мыши во всех режимах

-- 5. Производительность
opt.lazyredraw = true -- Перерисовывать экран только при необходимости
opt.updatetime = 250 -- Быстрая реакция на изменения
opt.redrawtime = 10000 -- Увеличить время перерисовки для сложных файлов (например, с длинными строками)

-- 6. Буферы файлов
opt.autoread = true -- Автоматически читать изменения файла с диска
opt.hidden = true -- Разрешить переключение между не сохранёнными буферами

-- 7. Дополнительные пользовательские параметры
opt.completeopt = "menuone,noselect" -- Настройки автодополнения: показывать меню, но не выбирать автоматически
opt.splitright = true -- Вертикальные сплиты будут открываться справа
opt.splitbelow = true -- Горизонтальные сплиты будут открываться снизу
opt.scrolloff = 8 -- Оставлять 8 строк вокруг курсора при скроллинге
opt.sidescrolloff = 8 -- Оставлять 8 столбцов при горизонтальном скроллинге
opt.conceallevel = 0 -- Полное отображение текста, без скрытых символов
opt.backup = false -- Отключить резервные копии файлов

-- Параметры отображения
opt.signcolumn = "yes" -- Всегда показывать столбец с диагностикой знаков
